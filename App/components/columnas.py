class Columnas(object):
    def __init__(self, columnas = 2):
        self._columnas = columnas

    def get_columnas(self):
        return self._columnas

    def set_columnas(self, columnas):
        self._columnas = columnas

    def cifrar(self, cadena):
        cripto = cadena
        return cripto

    def descifrar(self, cadena):
        mcla = cadena
        return mcla
